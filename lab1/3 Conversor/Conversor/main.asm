.include "m328pdef.inc"

.org 0x0000
rjmp RESET

RESET:
    ; Stack
    ldi r16, high(RAMEND)
    out SPH, r16
    ldi r16, low(RAMEND)
    out SPL, r16

    ; PORTD como salida
    ldi r16, 0xFF
    out DDRD, r16

    ; PORTD = 0
    clr r16
    out PORTD, r16

    ; Apuntar Z al inicio de la LUT
    ldi ZH, high(LUT_START<<1)
    ldi ZL, low(LUT_START<<1)

    rjmp MAIN_LOOP

MAIN_LOOP:
    ; Leer siguiente valor de la LUT y avanzar puntero
    lpm r16, Z+
    out PORTD, r16

    rcall delay

    ; ¿Llegamos al final?
    ldi r30, low(LUT_END<<1)   ; ZL objetivo
    ldi r31, high(LUT_END<<1)  ; ZH objetivo
    cp  ZL, r30
    cpc ZH, r31
    brne MAIN_LOOP             ; si no es fin, seguir

    ; Volver al inicio de la tabla
    ldi ZH, high(LUT_START<<1)
    ldi ZL, low(LUT_START<<1)
    rjmp MAIN_LOOP

delay:
	ldi  r22, 82
    ldi  r23, 9
L1: dec  r23
    brne L1
    dec  r22
    brne L1
	nop
	ret

